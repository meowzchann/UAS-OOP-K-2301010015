    private void tblBookingsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBookingsMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblBookings.getSelectedRow();
        
        // Ensure a row is actually selected
        if (selectedRow != -1) {
            // Get data from the table model and set it to the text fields
            // Note: The ID is not editable, so we don't need a text field for it,
            // but we retrieve it to know which record to update/delete.
            txtName.setText(tableModel.getValueAt(selectedRow, 1).toString());
            txtPhone.setText(tableModel.getValueAt(selectedRow, 2).toString());
            txtDate.setText(tableModel.getValueAt(selectedRow, 3).toString());
            txtPackage.setText(tableModel.getValueAt(selectedRow, 4).toString());
        }
      
    }//GEN-LAST:event_tblBookingsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblBookings.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to update.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Get the ID of the selected booking from the table model
        int bookingId = (int) tableModel.getValueAt(selectedRow, 0);

        // SQL query to update a record
        String sql = "UPDATE bookings SET customer_name = ?, phone_number = ?, book_date = ?, package_type = ? WHERE id = ?";

        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement pstmt = conn.prepareStatement(sql);
            
            // Set the parameters for the query from the text fields
            pstmt.setString(1, txtName.getText());
            pstmt.setString(2, txtPhone.getText());
            pstmt.setString(3, txtDate.getText());
            pstmt.setString(4, txtPackage.getText());
            pstmt.setInt(5, bookingId); // Use the ID for the WHERE clause

            // Execute the update
            int rowsAffected = pstmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Data updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                // Refresh the table to show the updated data
                loadTableData();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            pstmt.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
       
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      int selectedRow = tblBookings.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Confirmation dialog to prevent accidental deletion
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            // Get the ID of the selected booking
            int bookingId = (int) tableModel.getValueAt(selectedRow, 0);

            // SQL query to delete a record
            String sql = "DELETE FROM bookings WHERE id = ?";

            try {
                Connection conn = koneksi.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setInt(1, bookingId);

                int rowsAffected = pstmt.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Record deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    // Refresh table data
                    loadTableData();
                    // Clear text fields
                    txtName.setText("");
                    txtPhone.setText("");
                    txtDate.setText("");
                    txtPackage.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete record.", "Error", JOptionPane.ERROR_MESSAGE);
                }
                
                pstmt.close();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

