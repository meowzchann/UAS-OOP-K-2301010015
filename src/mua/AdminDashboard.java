    private void loadTableData() {
        // Define column headers for the table
        tableModel = new DefaultTableModel();
        tableModel.addColumn("ID");
        tableModel.addColumn("Customer Name");
        tableModel.addColumn("Phone Number");
        tableModel.addColumn("Book Date");
        tableModel.addColumn("Package");
        tableModel.addColumn("Booking Time");
        
        tblBookings.setModel(tableModel);

        try {
            // Get connection from our koneksi helper class
            Connection conn = koneksi.getConnection();
            Statement stmt = conn.createStatement();
            // SQL query to select all records from the bookings table
            String sql = "SELECT * FROM bookings";
            ResultSet rs = stmt.executeQuery(sql);

            // Clear the table before loading new data
            tableModel.setRowCount(0);

            // Iterate through the result set and add rows to the table model
            while (rs.next()) {
                tableModel.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("customer_name"),
                    rs.getString("phone_number"),
                    rs.getString("book_date"),
                    rs.getString("package_type"),
                    rs.getString("booking_time")
                });
            }
            
            // Close resources
            rs.close();
            stmt.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading data from database: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

